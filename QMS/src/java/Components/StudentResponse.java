    /**
     * Handles the HTTP <code>POST</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doPost(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        response.setContentType("text/html;charset=UTF-8");
        try (PrintWriter out = response.getWriter()) {
            String name,contact,response1;
            name = request.getParameter("stuName");
            contact = request.getParameter("contact");
            response1 = request.getParameter("response");
            Student s = new Student();
            s.setStuName(name);
            s.setStuContact(contact);
            s.setResponse(response1);
            
            
            int status = StudentDb.saveResponse(s);
            
            /* TODO output your page here. You may use following sample code. */
            out.println("<!DOCTYPE html>");
            out.println("<html>");
            out.println("<head>");
            out.println("<title>Servlet RegisterServlet</title>");            
            out.println("</head>");
            out.println("<body>");
            if(status > 0)
            {
                HttpSession httpSession = request.getSession();
                httpSession.setAttribute("message5", "Response sent successfully!!");
                response.sendRedirect("StudentLog.jsp");
                return;
            }
            else{
                HttpSession httpSession = request.getSession();
                httpSession.setAttribute("message6", "Response Failed");
                response.sendRedirect("StudentLog.jsp");
                
            }
            out.println("</body>");
            out.println("</html>");
            out.close();
        }
    }

